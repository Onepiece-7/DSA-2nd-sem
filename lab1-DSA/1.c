
// WAP to display n elements in 10 array

#include <stdio.h>
int main()
{
    int i, a[10];
    printf("Enter the elements for the array:\n");
    for (i = 0; i < 10; i++)
    {
        scanf("%d", &a[10]);
    }
    printf("The elements present in the array are:\n");
    for (i = 0; i < 10; i++)
    {
        printf("%d\t", a[i]);
    }
}
